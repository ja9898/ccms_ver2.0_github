<h3  style="color:black"><u>Policy Guidelines</u></h3>
<ol style="margin:14px; display: inline-block;padding: 5px;">
	<li style="margin-bottom:3px">Permanent employees of Zeb Fortunes are eligible for these leaves. Internees shall not be eligible. However exceptions if any will be facilitated on case to case basis.</li>
	<li style="margin-bottom:3px">During the probationary period of first three (3) months, employees are also entitled to get Casual leaves, but they will not avail any sick leave, as per above mentioned plan. However, exception if any shall be dealt on case to case basis.</li>
	<li style="margin-bottom:3px">Employees are entitled for a total of twelve causal leaves divided over a calendar year (January to December). Can avail one casual leave per month for which there shall be no deduction from salary (if they will provide approved leave form). In case employee doesn't take any Casual Leave for that specific month he/she shall be granted one day extra salary.</li>
	<li style="margin-bottom:3px">For medical leaves employees must submit MC (Medical Certificate) in HR.</li>
	<li style="margin-bottom:3px">If any employee takes a leave on last working day before a public holiday and on the first working day after that holiday, all the days including that holiday(s) will be treated as leaves for that employee.</li>
	<li style="margin-bottom:3px">Managers need to ensure their leave will not affect any working mechanism before going on leave. Only 3 employees from each department will be entertained at one time on first come first serve bases to ensure smooth operations.</li>
	<li style="margin-bottom:3px">Employees shall coordinate within their respective tier to hand over his/her duties to ensure smooth working mechanism during his leave. For instance GM is to be relieved by Shift Manager (day or night), AGM (day) is to relieved by Floor Manager (Day), AGM (Night) is to be relieved by Floor Manager (night), Manager HR is to be relieved by HR Assistant, Manager Finance is to be relieved by Accounts Officers and vice versa.</li>
	<li style="margin-bottom:3px">Once an employee has availed his/her Casual Leave for a that month ,thereafter, for each extra leave his/her salary will be deducted.</li>
	<li style="margin-bottom:3px">Compensation for Casual Leave is his/her one day salary. There is no encashment for Medical Leaves.</li>
	<li style="margin-bottom:3px">All Employees are required to adhere to their respective office timings. Anyone coming late shall be highlighted in the Attendance register for that day. Every four (4) late comings(10 mints to 60 mints) will be considered  as one day leave, which will not be adjusted  against  Annual Leave, however  this can be adjusted  against Casual Leave for that  month  or else   the deduction shall be  done from  the month salary. While late coming after 1 hour -4hours will be considering as1 short leave.</li>
	<li style="margin-bottom:3px">Two half leaves will be equal to one full day. Total one to four consecutive hours of absence from office in a single day will be considered as one half leave. This can be adjusted against Casual Leave only.</li>
	<li style="margin-bottom:3px">Only those leaves will be entertained for which signed application form will be received within 3 days after that leave.</li>
	<li style="margin-bottom:3px">If any employee will not inform about his absence in written. She/he will be charged 200 Pkr as fine. Whereas Emergency leave or Medical leave will be dealt after reporting back to company.</li>
	<li style="margin-bottom:3px">Every employee should inform his/her concerning department by ringing them while taking any emergency or Medical leave. And then department responsibility is there to inform HR Department on the same day by emailing.</li>
